# while condition :
# statement to be execute (execution continue until condition turns false)

i = 1
while i <= 10:
    print(i)
    i += 1
# Important: make sure loop variable changes, otherwise it can cause infinite loop

print ("Loop ended")

print("\n---REVERSE LOOP---")

j = 10
while j >= 1:
    print(j)
    j -= 1

print("Loop Ended")