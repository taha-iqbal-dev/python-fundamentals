# Indexing - accessing individual characters of a string using their position

str1 = "TAHA IQBAL"
# Index starts from 0 and goes up to length-1 (includes spaces as characters)

print(str1)
print("Character at Index 0: ", str1[0])
print("Character at Index 4: ", str1[4]) # the index is space
print("Character at Index -1 -- Last Character: ", str1[-1])